package springioc.homework1;

public class Friend {

	private String name;
	private int age;
	
	public Friend() {
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getAge() {
		return age;
	}

	public String getName() {
		return name;
	}
	
}
